package application;

public class App {

	public static void main(String[] args) {
		//Boolean is a primitive variable type
		boolean condition1 = false;
		System.out.println("Condition 1: " + condition1);
		
		//Can set the boolean variable to an expression
		boolean condition2 = 4 < 5;
		System.out.println("Condition 2: " + condition2);

		boolean condition3 = 8 < 5;
		System.out.println("Condition 2: " + condition3);
		
		boolean condition4 = 4 == 5;
		System.out.println("Condition 2: " + condition4);
	}

}
